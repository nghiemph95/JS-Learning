import { findSumPair } from "./12-findSumPair";

describe("Name of the group", () => {
  test("should ", () => {
    expect(findSumPair([])).toStrictEqual([]);
  });

  test("should ", () => {
    expect(findSumPair([], 10)).toStrictEqual([]);
  });

  test("should ", () => {
    expect(findSumPair([1, 2], 2)).toStrictEqual([]);
  });

  test("should ", () => {
    expect(findSumPair([3, 2, 1], 5)).toStrictEqual([3, 2]);
  });

  test("should ", () => {
    expect(findSumPair([3, 3, 2, 1], 6)).toStrictEqual([3, 3]);
  });
});
