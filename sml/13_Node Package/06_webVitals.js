/** Core Web Vitals
 * - LCP: Largest Contentful Paint -> Loading: Tốc độ tải trang
 * - FID: First Input Delay -> Interactivity: Khả năng tương tác
 * - CLS: Cumulative Layout Shift -> Visual Stability: TÍnh ổn định của trang
 */

/**
 * LCP: Largest Contentful Paint
 * - Chỉ số đánh giá hiệu suất tải trang thông qua việc đo th gian
 * hoàn tất phần tử lớn nhất trên trang web
 * - LCP < 2.5s là tối ưu
 */

/**
 * FID: First Input Delay
 * - Chỉ số đo lường thời gian phản hồi lại tương tác đầu tiên
 * của người dùng trên trang web.
 * - FID < 100ms là tối ưu tốt
 */

/**
 * CLS: Cumulative Layout Shift
 * - Chỉ số đánh gái mức độ dịch chuyển bất ngờ của các phần tử 
 * trên trang web mà người dùng không mong muốn
 * - CLS: 0.1 đến 0.25s là tạm chấp nhận
 */
